# Даны координаты поля шахматной доски х, у (целые числа,
# лежащие в диапазоне 1-8).
# Учитывая, что левое нижнее поле доски (1,1) является черным,
# проверить истинность высказывания: «Данное поле является белым».

while True:
    try:
        x = int(input("Введите координату x (1-8): "))
        y = int(input("Введите координату y (1-8): "))

        if 1 <= x <= 8 and 1 <= y <= 8:
            break
        else:
            print("Ошибка: координаты должны быть в диапазоне 1-8!")
    except ValueError:
        print("Ошибка: введите целые числа!")

if (x + y) % 2 == 1:
    print(f"Данное поле с координатами ({x}, {y}) является белым")
else:
    print(f"Данное поле с координатами ({x}, {y}) не является белым")