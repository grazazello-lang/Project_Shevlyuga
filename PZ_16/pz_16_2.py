class Автомобиль:
    """Базовый класс для всех типов автомобилей"""
    def __init__(self, марка: str, модель: str, год_выпуска: int):
        self.марка = марка
        self.модель = модель
        self.год_выпуска = год_выпуска

    def __str__(self):
        return f"Автомобиль: {self.марка} {self.модель}, Год выпуска: {self.год_выпуска}"


class Грузовик(Автомобиль):
    """Класс Грузовик, наследуемый от Автомобиля"""
    def __init__(self, марка: str, модель: str, год_выпуска: int, грузоподъемность: float):
        # Вызываем конструктор родительского класса для инициализации общих полей
        super().__init__(марка, модель, год_выпуска)
        self.грузоподъемность = грузоподъемность

    def __str__(self):
        # Получаем базовую строку из родительского класса и дополняем её
        base_info = super().__str__()
        return f"{base_info}, Тип: Грузовик, Грузоподъемность: {self.грузоподъемность} т"


class ЛегковойАвтомобиль(Автомобиль):
    """Класс Легковой автомобиль, наследуемый от Автомобиля"""
    def __init__(self, марка: str, модель: str, год_выпуска: int, количество_пассажиров: int):
        # Вызываем конструктор родительского класса
        super().__init__(марка, модель, год_выпуска)
        self.количество_пассажиров = количество_пассажиров

    def __str__(self):
        # Получаем базовую строку из родительского класса и дополняем её
        base_info = super().__str__()
        return f"{base_info}, Тип: Легковой, Количество пассажиров: {self.количество_пассажиров}"


