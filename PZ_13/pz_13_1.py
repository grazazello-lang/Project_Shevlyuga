# Список произведений Достоевского
import re

# В исходном текстовом файле(Dostoevsky.txt) найти все произведения писателя.
# Посчитать количество полученных элементов.

# Читаем файл
with open('Dostoevsky.txt', 'r', encoding='utf-8') as file:
    text = file.read()

# Ищем все названия в кавычках (русские «» и "")
# Паттерн ищет текст внутри кавычек
works_found = re.findall(r'[«"]([^«"]*?)[»"]', text)

# Фильтруем - убираем короткие слова и дубликаты
works_found = list(set(works_found))  # убираем дубликаты

# Сортируем по алфавиту
works_found.sort()

# Выводим результаты
print("Найденные произведения Достоевского:")
print("=" * 50)
for i, work in enumerate(works_found, 1):
    print(f"{i}. {work}")

print("=" * 50)
print(f"Всего найдено произведений: {len(works_found)}")
print(f"Количество полученных элементов: {len(works_found)}")