# Даны два целых числа A и B (A < B).
# Вывести в порядке возрастания все целые
# числа, расположенные между A и B (включая сами числа A и B),
# а также количество N этих чисел.
try:
    # Ввод чисел A и B
    A = int(input("Введите целое число A: "))
    B = int(input("Введите целое число B: "))

    # Проверка условия A < B без raise
    if A >= B:
        print("Ошибка: A должно быть строго меньше B")
    else:

        # Сохраняем исходное значение A для подсчета
        first = A
        amount = 0

        # Вывод чисел в порядке возрастания
        print(f"Все целые числа от {A} до {B} в порядке возрастания:")

        # Выводим числа с помощью цикла while
        while first <= B:
            print(first, end=" ")
            first += 1
            amount += 1

        print(f"\nКоличество чисел = {amount}")

except ValueError:
    print("Ошибка: Введите корректные целые числа!")