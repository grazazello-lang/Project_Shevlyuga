# Из предложенного текстового файла (text18-25.txt) вывести на экран его содержимое,
# количество символов, принадлежащих к группе букв. Сформировать новый файл, в
# который поместить текст в стихотворной форме предварительно удалив букву «с» из
# текста.

# Выводим содержимое файла на экран, считаем количество букв
t = 0  # счетчик букв
print("Содержимое файла text18-25.txt:")
print()

for i in open('text18-25.txt', encoding='UTF-8'):
    print(i, end='')
    for j in i:
        if j.isalpha():  # проверяем, принадлежит ли символ к группе букв
            t += 1

print()
print()
print('Количество символов, принадлежащих к группе букв:', t)

# Читаем файл построчно
f1 = open('text18-25.txt', encoding='UTF-8')
l = f1.readlines()
f1.close()

# Удаляем букву «с» из каждой строки
for i in range(len(l)):
    new_line = ''
    for j in l[i]:
        if j != 'с' and j != 'С':  # удаляем и строчную, и заглавную букву «с»
            new_line = new_line + j
    l[i] = new_line

# Записываем результат в новый файл
f2 = open('text18-25_new.txt', 'w', encoding='UTF-8')
f2.writelines(l)
f2.close()

print()
print('Новый файл text18-25_new.txt создан. Буква "с" удалена.')