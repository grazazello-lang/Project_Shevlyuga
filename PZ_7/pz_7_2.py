# Даны строки S, S1 и S2. Заменить в строке S последнее вхождение строки S1
# на строку S2.
S = input("Введите строку S: ")
S1 = input("Введите строку S1: ")
S2 = input("Введите строку S2: ")

print(f"S  = '{S}'")
print(f"S1 = '{S1}'")
print(f"S2 = '{S2}'")

# Находим последнее вхождение S1 в S
last_index = S.rfind(S1)

if last_index != -1:
    # Заменяем последнее вхождение
    result = S[:last_index] + S2 + S[last_index + len(S1):]
    print(f"Последнее вхождение '{S1}' найдено на позиции {last_index}")
    print(f"Результат замены: '{result}'")
else:
    print(f"Строка '{S1}' не найдена в '{S}'")
    print(f"Результат без изменений: '{S}'")

