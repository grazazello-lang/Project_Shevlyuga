# Дано целое число N (>0) и строка S. Преобразовать строку S в строку длины N
# следующим образом: если длина строки S больше N, то отбросить первые символы,
# если длина строки S меньше N, то в ее начало добавить символы «.» (точка).

try:
    N = int(input("Введите число N (>0): "))
    S = input("Введите строку S: ")
    long = len(S)
    print(f"N = {N}")
    print(f"S = '{S}'")
    print(f"Длина S = {long}")

    if long > N:
        result = S[-N:]
        print(f"Длина S > N: отбрасываем первые {long - N} символов")
    else:
        tochki = N - long
        result = "." * tochki + S
        print(f"Длина S < N: добавляем {tochki} точек в начало")

    print(f"Результат: '{result}'")
except ValueError:
    print("Проверьте правильность введенных данных!")